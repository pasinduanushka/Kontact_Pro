    private void jLabel20MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel20MouseClicked
        // dlete current contact
        String uId = account.getText().toLowerCase(); //get loged user id
        String contact = contactID.getText(); //get current contact details id
        int contID = Integer.parseInt(contact);

        //get all data to the arraylist
        Core core = new Core();
        int size = core.dataLoad(uId);

        ArrayList<Contacts> list = core.getList();

        //remove current contact from arraylist
        list.remove(contID);

        ///////////clear the file////////////////////
        File file = new File("Contacts_" + uId + ".txt");
        PrintWriter writer;
        try {
            writer = new PrintWriter(file);
            writer.print("");
            writer.close();
        } catch (FileNotFoundException ex) {
            System.out.println("file not found");
            Logger.getLogger(Details_Edit.class.getName()).log(Level.SEVERE, null, ex);
        }

        //add array data to file
        FileWriter fw = null;
        try {
            File flie = new File("Contacts_" + uId + ".txt");
            fw = new FileWriter(flie, true);

            PrintWriter pw = new PrintWriter(fw);

            String all = "";
            for (int i = 0; i < size - 1; i++) {

                //write new contact details in text file
                pw.println(i + "ÿ" + core.encrypt(list.get(i).getfName()) + "ÿ" + core.encrypt(list.get(i).getlName()) + "ÿ" + core.encrypt(list.get(i).getMobileNo()) + "ÿ" + core.encrypt(list.get(i).getHomeNo()) + "ÿ" + core.encrypt(list.get(i).getEmail()) + "ÿ" + core.encrypt(list.get(i).getGender()) + "ÿ" + core.encrypt(list.get(i).getbDay()) + "ÿ" + core.encrypt(uId));

            }
            pw.flush();
            JOptionPane.showMessageDialog(this, "Contact Deleted Successfully!", "Deleted", WIDTH);

            Search_Main seMain = new Search_Main(uId);
            seMain.setLocationRelativeTo(this);
            seMain.setVisible(true);
            this.dispose();

        } catch (IOException ex) {
            Logger.getLogger(Add_Contact.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                fw.close();
            } catch (IOException ex) {
                Logger.getLogger(Add_Contact.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_jLabel20MouseClicked

