    User_Details(String uId, String item) {
        initComponents();
        setIcon(); //set favicon icon
        account.setText(uId); //set log user name

        String gender;

        Core core = new Core();
        int size = core.dataLoad(uId);

        ArrayList<Contacts> list = core.getList();

        // set user details in text fields
        for (int i = 0; i < size; i++) {

            String fullName = list.get(i).getContactId();
            if (fullName.equals(item)) {

                jLabel14.setText(list.get(i).getfName() + " " + list.get(i).getlName());
                jLabel13.setText(list.get(i).getMobileNo());
                jLabel17.setText(list.get(i).getHomeNo());
                jLabel15.setText(list.get(i).getEmail());

                //check gender m for male and f for female
                if (list.get(i).getGender().equalsIgnoreCase("m")) {
                    gender = "Male";
                } else {
                    gender = "Female";
                }
                jLabel18.setText(gender);
                jLabel16.setText(list.get(i).getbDay());
                contactID.setText(list.get(i).getContactId());

            }

        }

    }

