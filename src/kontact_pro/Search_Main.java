    Search_Main(String uId) {
        initComponents();
        setIcon(); //set favicon icon

        account.setText(uId); //set user name

        list1.clear();
        Core core = new Core();
        core.dataLoad(uId);

        ArrayList<Contacts> list = core.getList();
        int size = core.dataLoad(uId);

        ArrayList<String> contactSort = new ArrayList<String>();

        // add all contacts to the list
        for (int i = 0; i < size; i++) {

             if (list.get(i).getUserRef().equalsIgnoreCase(uId)) {
            contactSort.add(list.get(i).getfName() + " " + list.get(i).getlName());
             }
        }

        Collections.sort(contactSort); //sort the result

        //print the result
        for (String conString: contactSort) {
            list1.add(conString);
        }

    }

