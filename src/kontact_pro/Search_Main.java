    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:

        String x = jTextField1.getText(); // get search text
        String uId = account.getText().toLowerCase(); // get user id

        int count = 0;

        list1.clear(); //clear the list before search new
        Core core = new Core();
        int size = core.dataLoad(uId);

        ArrayList<Contacts> list = core.getList();

        // print the result searching
        for (int i = 0; i < size; i++) {

            if (list.get(i).getUserRef().equalsIgnoreCase(uId)) {
            
            String fullName = list.get(i).getfName() + " " + list.get(i).getlName();
            if (fullName.toLowerCase().startsWith(x)) {
                list1.add(fullName, count);
                count++;
            }

            }
        }


    }//GEN-LAST:event_jTextField1KeyReleased

